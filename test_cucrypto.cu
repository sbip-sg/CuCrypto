// test cgbn_add

#include <stdio.h>
#include <stdint.h>
#include <stdlib.h>
#include <CuCrypto/keccak.cuh>
#include <CuCrypto/sha256.cuh>


int main() {
  uint8_t data[32] = {0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8, 0x9, 0xa, 0xb, 0xc, 0xd, 0xe, 0xf, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f, 0x20};
  uint8_t hash[32];
  CuCrypto::keccak::sha3(data, 1, hash, 32);
  for (int i = 0; i < 32; i++) {
    printf("%02x", hash[i]);
  }
  printf("\n");
  CuCrypto::sha256::sha(data, 1, hash);
  for (int i = 0; i < 32; i++) {
    printf("%02x", hash[i]);
  }
  printf("\n");
  return 0;
}